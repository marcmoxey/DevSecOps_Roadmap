# for Loops and the range() function 
print('Hello')
for i in range(5):
    print('On this iteration, i is set to ' + str(i))
print('Goodbye')

print('Hello!')
i = 0 
while i < 5:
    print('On this iteration, i is set to ' + str(i))
    i = i + 1
